    private void btn_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editarActionPerformed
       try {
             String update = "UPDATE personagens SET nome= ? WHERE id = ?";
              String update1 = "UPDATE personagens SET idade= ? WHERE id = ?";
             String update2 = "UPDATE personagens SET aldeia= ? WHERE id = ?";
             String update3 = "UPDATE personagens SET status= ? WHERE id = ?";
             String update4 = "UPDATE personagens SET observacao = ? WHERE id = ?";

             PreparedStatement psmt = con.prepareStatement(update);
             psmt.setString(1, nome_txt.getText().isEmpty() ? null : nome_txt.getText());
             psmt.setInt(2, id_txt.getText().isEmpty() ? 0 : Integer.parseInt(id_txt.getText()));
             int rowsAffected = psmt.executeUpdate();
             
             PreparedStatement psmt1 = con.prepareStatement(update1);
             psmt1.setInt(1, idade_txt.getText().isEmpty() ? 0 : Integer.parseInt(idade_txt.getText()));
             psmt1.setInt(2, id_txt.getText().isEmpty() ? 0 : Integer.parseInt(id_txt.getText()));
             rowsAffected += psmt1.executeLargeUpdate();

             PreparedStatement psmt2 = con.prepareStatement(update2);
             psmt2.setString(1, aldeia_txt.getText().isEmpty() ? null : aldeia_txt.getText());
             psmt2.setInt(2, id_txt.getText().isEmpty() ? 0 : Integer.parseInt(id_txt.getText()));
             rowsAffected += psmt2.executeLargeUpdate();

             PreparedStatement psmt3 = con.prepareStatement(update3);
             psmt3.setString(1, status_txt.getText().isEmpty() ? null : status_txt.getText());
             psmt3.setInt(2, id_txt.getText().isEmpty() ? 0 : Integer.parseInt(id_txt.getText()));
             rowsAffected += psmt3.executeUpdate();

             PreparedStatement psmt4 = con.prepareStatement(update4);
             psmt4.setString(1, obs_txt.getText().isEmpty() ? null : obs_txt.getText());
             psmt4.setInt(2, id_txt.getText().isEmpty() ? 0 : Integer.parseInt(id_txt.getText()));
             rowsAffected += psmt4.executeUpdate();
             
             String mensagem = rowsAffected > 0 ? "Sucesso" : "Erro";
             campo_txt.setText(mensagem);
             } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btn_editarActionPerformed

    private void btn_consultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_consultaActionPerformed
        try {
   
    String query = "SELECT * from personagens WHERE id = ?";
    PreparedStatement psmt = con.prepareStatement(query);
    psmt.setInt(1, Integer.parseInt(id_txt.getText()));
    
    ResultSet rs = psmt.executeQuery();

    while (rs.next()) {
        String nome = rs.getString("nome");
        nome_txt.setText(nome);
        String idade = rs.getString("idade");
        idade_txt.setText(idade);
        String aldeia = rs.getString("aldeia");
        aldeia_txt.setText(aldeia);
        String status = rs.getString("status");
        status_txt.setText(status);
        String obs = rs.getString("observacao");
        obs_txt.setText(obs);
        
    }

} catch (SQLException ex) {
    ex.printStackTrace();
}
    }//GEN-LAST:event_btn_consultaActionPerformed

