    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
       try {
        String insert = "INSERT INTO personagens(nome, idade, aldeia, status, observacao) VALUES (?,?,?,?,?)";
       PreparedStatement psmt = con.prepareStatement(insert);
       
       psmt.setString(1, nome_txt.getText());
       psmt.setInt(2, Integer.parseInt(idade_txt.getText()));
       psmt.setString(3, aldeia_txt.getText());
       psmt.setString(4, status_txt.getText());
       psmt.setString(5, obs_txt.getText());
       
       int rowsAffected = psmt.executeUpdate();
       if(rowsAffected ==1 ){
           campo_txt.setText("Sucesso");
       }
       else {
           campo_txt.setText("Erro!");
       }
       } catch (SQLException e){
           JOptionPane.showMessageDialog(null, e);
       }
    }//GEN-LAST:event_btn_addActionPerformed

